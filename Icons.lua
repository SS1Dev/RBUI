-- Load icons from a table
function Icons.LoadLibrary(iconTable)
    for name, symbol in pairs(iconTable) do
        Icons.Map[name] = symbol
    end
end

-- Set default icon
function Icons.SetDefault(symbol)
    Icons.Default = symbol
end

-- Clear all icons
function Icons.Clear()
    Icons.Map = {}
end

